    private void loadReceptionistId() {
        try
        {
            Map<String,String> recepList=ReceptionistDAO.getAllReceptionistId();
            Set<String> keys=recepList.keySet();
            
            for(String id : keys)
            {
                cbReceptionistId.addItem(id);
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"DB Error","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
