    private void btnDeleteReceptionistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteReceptionistActionPerformed
        String userid=cbReceptionistId.getSelectedItem().toString();
        
        try
        {
            boolean result=ReceptionistDAO.deleteReceptionist(userid);
            if(result)
            {
                JOptionPane.showMessageDialog(null,"Receptionist deleted successfully");
                cbReceptionistId.removeItemAt(cbReceptionistId.getSelectedIndex());
                if(cbReceptionistId.getItemCount()==0)
                    btnDeleteReceptionist.setEnabled(false);
                return ;
            }
            
            
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"DB Error","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_btnDeleteReceptionistActionPerformed

