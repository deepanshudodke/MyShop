    private void loadReceptionistDetails() {
        try
        {
            receptionist=ReceptionistDAO.getNonRegisteredReceptionist();
            if(receptionist.isEmpty())
            {
                    JOptionPane.showMessageDialog(null,"No un-registered receptionist present");
                    btnAddReceptionist.setEnabled(false);
                    return;
            }
            
            btnAddReceptionist.setEnabled(true);
            
            Set<String> keys=receptionist.keySet();
            Iterator <String> it=keys.iterator();
            cbEmpId.removeAllItems();
            
            while(it.hasNext())
            {
                cbEmpId.addItem(it.next());
            }     
        }
        
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"DB Error","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }

