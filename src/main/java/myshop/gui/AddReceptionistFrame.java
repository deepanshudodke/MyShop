    private void btnAddReceptionistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddReceptionistActionPerformed
        int result=validateInputs();
        
        if(result==0)
        {
            JOptionPane.showMessageDialog(null,"Password must be atleast 5 character long");
            return;
        }
        
        else if(result==-1)
        {
            JOptionPane.showMessageDialog(null,"User ID cannot be left blank");
             return;
        }
        
        char []pwd1=txtPass.getPassword();
        char []pwd2=txtRePass.getPassword();
        
        if(isPasswordMatching(String.valueOf(pwd1),String.valueOf(pwd2))==false)
        {
            JOptionPane.showMessageDialog(null,"Passwords Not Matching");
                
             return;
        }
        
        try
        {
            String id=cbEmpId.getSelectedItem().toString();
            String userid=txtUserId.getText();
            String username=txtEmpName.getText();
            UserPojo u=new UserPojo();
            
            u.setEmpid(id);
            u.setUserid(userid);
            u.setUsername(username);
            String spwd=String.valueOf(pwd1);
            u.setPassword(spwd);
            u.setUsertype("Receptionist");
            
            boolean status=ReceptionistDAO.addReceptionist(u);
            if(status)
            {
                JOptionPane.showMessageDialog(null,"Receptionist added successfully");
                clearText();
                loadReceptionistDetails();
                
                return;
            }
            
            JOptionPane.showMessageDialog(null,"Receptionist not added");
        }
        
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"DB Error","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        

          


    }//GEN-LAST:event_btnAddReceptionistActionPerformed

